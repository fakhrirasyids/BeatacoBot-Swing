    private void tblUsersMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblUsersMouseClicked
        int row = tblUsers.getSelectedRow();
        long idUser = Long.parseLong(String.valueOf(tblUsers.getValueAt(row, 0)));
        showUserLogMessages(idUser);
        btnDelete.setEnabled(true);

        setLogMessageContent(true);
    }//GEN-LAST:event_tblUsersMouseClicked

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tblUsers.getSelectedRow();
        long idUser = Long.parseLong(String.valueOf(tblUsers.getValueAt(row, 0)));

        if (userTableHandler.deleteUser(idUser)) {
            showUsers();
            tblMessages.setModel(new DefaultTableModel());
            setLogMessageContent(false);
        } else {
            showMessageDialog(null, "Gagal hapus user!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

