    private void showUsers() {
        Object[][] allUsers = userTableHandler.getAllUsers();
        tblUsers.setModel(new DefaultTableModel());
        tblUsers.setModel(new DefaultTableModel(allUsers, header));
    }

    private void showUserLogMessages(long idUser) {
        Object[][] allMessages = messageTableHandler.getAllUserLogMessages(idUser);
        tblMessages.setModel(new DefaultTableModel());
        tblMessages.setModel(new DefaultTableModel(allMessages, headerMessages));
    }

    private void setLogMessageContent(boolean isEnabled) {
        tblMessages.setEnabled(isEnabled);
    }

    private void dataUpdater() {
        int delay = 1000;
        ActionListener taskPerformer = new ActionListener() {
            public void actionPerformed(ActionEvent evt) {
                int row = tblUsers.getSelectedRow();

                if (row >= 0) {
                    long idUser = Long.parseLong(String.valueOf(tblUsers.getValueAt(row, 0)));
                    showUserLogMessages(idUser);
                }
            }
        };
        new Timer(delay, taskPerformer).start();
    }

    private void filter(String query) {
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(new DefaultTableModel());
        tblMessages.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(query));
    }
    
