    private void setTabButtonListener(int tabIndex) {
        if (tabIndex == 1) {
            btnCommands.setColor(btnColorTrue);
            btnCommands.setColorOver(btnColorTrue);
            btnCommands.setColorClick(btnColorTrue);
            btnCommands.setForeground(Color.BLACK);

            btnUsers.setColor(btnColorFalse);
            btnUsers.setColorOver(btnColorFalseHover);
            btnUsers.setColorClick(btnColorFalseClick);
            btnUsers.setForeground(Color.WHITE);

            btnBroadcast.setColor(btnColorFalse);
            btnBroadcast.setColorOver(btnColorFalseHover);
            btnBroadcast.setColorClick(btnColorFalseClick);
            btnBroadcast.setForeground(Color.WHITE);

            btnCommands.setEnabled(false);
            btnUsers.setEnabled(true);
            btnBroadcast.setEnabled(true);
        } else if (tabIndex == 2) {
            btnUsers.setColor(btnColorTrue);
            btnUsers.setColorOver(btnColorTrue);
            btnUsers.setColorClick(btnColorTrue);
            btnUsers.setForeground(Color.BLACK);

            btnCommands.setColor(btnColorFalse);
            btnCommands.setColorOver(btnColorFalseHover);
            btnCommands.setColorClick(btnColorFalseClick);
            btnCommands.setForeground(Color.WHITE);

            btnBroadcast.setColor(btnColorFalse);
            btnBroadcast.setColorOver(btnColorFalseHover);
            btnBroadcast.setColorClick(btnColorFalseClick);
            btnBroadcast.setForeground(Color.WHITE);

            btnCommands.setEnabled(true);
            btnUsers.setEnabled(false);
            btnBroadcast.setEnabled(true);
        } else {
            btnBroadcast.setColor(btnColorTrue);
            btnBroadcast.setColorOver(btnColorTrue);
            btnBroadcast.setColorClick(btnColorTrue);
            btnBroadcast.setForeground(Color.BLACK);

            btnCommands.setColor(btnColorFalse);
            btnCommands.setColorOver(btnColorFalseHover);
            btnCommands.setColorClick(btnColorFalseClick);
            btnCommands.setForeground(Color.WHITE);

            btnUsers.setColor(btnColorFalse);
            btnUsers.setColorOver(btnColorFalseHover);
            btnUsers.setColorClick(btnColorFalseClick);
            btnUsers.setForeground(Color.WHITE);

            btnCommands.setEnabled(true);
            btnUsers.setEnabled(true);
            btnBroadcast.setEnabled(false);
        }
    }


